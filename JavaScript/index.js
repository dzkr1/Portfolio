var photo = [];
for (var i = 0; i < 30; i++) {
    photo[i] = document.getElementById("foto" + i);
}

for (var j = 0; j < photo.length; j++) {
    photo[j].onmouseover = function() {
        this.style.filter = "grayscale(0)";
    };

    photo[j].onmouseout = function() {
        this.style.filter = "grayscale(100%)";
    };
}
